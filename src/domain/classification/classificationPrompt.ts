import type { ClassificationInput } from '../types';

export function buildClassificationPrompt(input: ClassificationInput): string {
  return `당신은 사용자의 생산성 도구 사용 맥락을 분석하는 분류기입니다.

사용자가 작성한 메모 또는 일정의 제목과 내용을 보고 user_type 을 선택합니다.
가장 적합한 하나를 선택하되, 단서가 약하면 반드시 unknown 을 선택합니다.

[user_type]
- 대학생: 강의, 시험, 과제, 팀플, 동아리, 학점, 수업 등 학업 맥락
- 직장인: 회의, 보고, 미팅, 업무, 결재, 출장 등 조직 내 업무 맥락
- 프리랜서: 클라이언트, 프로젝트 납품, 견적, 외주, 개인 사업 맥락
- 팀 사용자: 우리 팀, 부서, 공동, 협업, 스프린트 등 복수 인원 협업 맥락
- unknown: 다음 중 하나라도 해당하면 선택
  1. 사적 영역(독서·운동·여가·소비·여행·취미·가계) 단서만 있을 때
  2. 내용이 비었거나 단일 행위 단어("준비", "정리", "기획")만 있을 때
  3. 두 개 이상 클래스의 단서가 비슷한 강도로 충돌할 때
  4. 학업·업무·외주·팀 협업 어느 쪽으로도 단정할 단서가 없을 때

[함정 키워드 — 단독 등장 시 분류 금지, unknown 우선]
- 직장인: '회의·미팅·프로젝트' 단독은 금지. 동호회·취미 모임도 같은 단어를 씀
- 대학생: '공부·학습·책·독서·~학·~론' 단독은 금지. 자기계발 독서도 같은 단어를 씀
- 팀 사용자: '회의·리뷰' 단독은 금지. '우리 팀/부서/공동/협업' 단서가 함께 있어야 함
- 프리랜서: '프로젝트' 단독은 금지. '클라이언트/외주/납품/견적/사업자' 단서가 함께 있어야 함

[입력]
제목: ${input.title}
내용: ${input.content}
타입: ${input.type}

[출력]
JSON으로만 답변하세요. 다른 필드 추가 금지.
{
  "user_type": "대학생|직장인|프리랜서|팀 사용자|unknown",
  "confidence": 0.0
}`;
}
